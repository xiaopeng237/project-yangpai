int i2c_wc(unsigned char data)
{
    int uiRet;
    unsigned char tx_buf[DATA_LEN];
    uiRet = ioctl(GiFd2631, I2C_SLAVE, I2C_ADDR >> 1);
    if (uiRet < 0) {
        I2CBUS_ERROR("setenv address faile ret: %x \n", uiRet);
        return -1;
    }
    tx_buf[0]=0x00;
    tx_buf[1]=data;
    int res = 0;
    res = write(GiFd2631, tx_buf, DATA_LEN); //写数据

    return 0;
}
